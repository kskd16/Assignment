package com.lpu.user.entity;



public class Product {

   
    private int id;
    private String pname;
    
    
    public Product() {}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
    
}